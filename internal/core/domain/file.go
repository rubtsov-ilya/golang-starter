// Package domain содержит доменные модели и бизнес-логику приложения.
// Это самый внутренний слой архитектуры — он не зависит ни от какого другого пакета проекта.
package domain

// File — доменная сущность для хранения содержимого файла (например, HTML-страницы).
// Поле buffer намеренно приватное — доступ только через метод Buffer(),
// что защищает внутреннее состояние от случайной модификации снаружи.
type File struct {
	buffer []byte
}

// Buffer возвращает байтовое содержимое файла
func (f *File) Buffer() []byte {
	return f.buffer
}

// NewFile создаёт новый File из переданного байтового буффера
func NewFile(buffer []byte) File {
	return File{
		buffer: buffer,
	}
}
